# CRM - Customer Relationship Management (Müşteri İlişkiler Yönetimi)
# Customer lifecycle/journey/funnel (yaşam döngüsü) optimizasyonu müşteri ve satıcı arasındaki süreci ifade eden döngülerdir
# Müşteri ile olan iletişimi ve etkileşimi görsel tekniklerle ifade edip, takip edilebilir forma getirmektir

# Daha fazla satış için müşteri edinme, müşteriyi elde tutma, cross-sell ve up-sell önemlidir
# Çeşitli platformlarda (dijital pazarlama, fiziksel pazarlama) reklam aracılığıyla müşteri edinilir
# Müşteriyi elde tutmak için daimi destek, süreçlere ilgi ve optimum performans gereklidir
# Cross-sell tamamlayıcı ürünleri satmaktır ve bir ürünü satarken yanında tamamlayıcı ürünler satarak kar artışı hedefler
# Up-sell ise satılan ürünün daha büyük veya yüksek kalite halini satmaya çalışmak ve kar artışı kazanmaktır.
# Son olarak müşteri segmentasyonu, müşterileri daha ufak gruplara ayırarak etkileşimi hepsine yönelik yapmaktansa,
# hedef gruplara odaklı iletişim kurularak, daha odaklı ve daha verimli iletişim kurulabilir

# CRM analitiği ise tüm müşteri ilişki sürecini veriye dayalı olarak daha verimli hale getirmeye çalışmaktır
# Verileri kullanarak stratejik çözümler geliştirmeyi ve daha az çabayla, daha çok kar elde etmeyi sağlar

