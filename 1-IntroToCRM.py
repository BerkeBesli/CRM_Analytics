# KPI - Key Performance Indicators (Temel Performans Göstergeleri)
# Şirket, departman ya da çalışanların performanslarını değerlendirmek için kullanılan matematiksel göstergelerdir.
# Bu göstergeler müşteri kazanımı, kaybı ve ilişki kalitesi hakkında bilgi verir.

# KPI Örnekleri;
# Customer Acquisiton Rate (Müşteri Kazanma Oranı) -> Belirli bir zamanda kazanılan müşteri yüzdesidir.
# Reklamlar veya bir pazarlama çalışması aracılığıyla belli sürede kazanılan müşterilere bakılır.

# Customer Retention Rate (Müşteri Elde Etme Oranı) -> Kullanıcının, müşteri belirlendiği andan itibaren müşteri kaldığı zamanın oranıdır
# Belli bir kullanıcının, 2. ürünü aldıktan sonra ne kadar süre alım yapmaya devam ettiğine bakılır.
# Veya bir kullanıcının uygulamayı kullanmaya başladığından beri ne kadar süredir kullanmaya devam ettiğine bakılır.

# Customer Churn Rate (Müşteri Terk Oranı) -> Müşterilerin terk edilme oranını numerik ifade edilişidir.

# Conversion Rate (Dönüşüm Oranı) -> Etkileşim yapılan potansiyel müşterilerin, ürün kullanmasının veya almasının oranıdır
# Ilana 10 kişi tıklar, 1 kişi ürünü alırsa bu conversion rate ile %10 şeklinde temsil edilir.
# 1000 kişi görür, 500 kişi tıklar, 50 kişi alır gibi durumlarda farklı değerlere endekslenebilir.

# Growth Rate (Büyüme Oranı) -> Şirketlerin orta-uzun vadeli planlar için genel hedeflerdir

